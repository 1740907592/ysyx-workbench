#include "local-include/reg.h"
#include <cpu/cpu.h>
#include <cpu/ifetch.h>
#include <cpu/decode.h>

#define R(i) gpr(i)
#define Mr vaddr_read
#define Mw vaddr_write

enum {
  TYPE_I, TYPE_U, TYPE_S,
  TYPE_R,
  TYPE_SB,
  TYPE_UJ,

  TYPE_N, // none
};
//后面有几个数字久移动多少位,然后进行符号扩展
#define src1R() do { *src1 = R(rs1); } while (0)
#define src2R() do { *src2 = R(rs2); } while (0)
#define immI() do { *imm = SEXT(BITS(i, 31, 20), 12);} while(0)
#define immU() do { *imm = SEXT(BITS(i, 31, 12), 20) << 12; } while(0)
#define immS() do { *imm = (SEXT(BITS(i, 31, 25), 7) << 5) | BITS(i, 11, 7); } while(0)
#define immJ() do { \
  *imm = (SEXT(BITS(i, 31, 31), 1) << 19) | \
         (BITS(i, 19, 12) << 11) | \
         (BITS(i, 20, 20) << 10) | \
         (BITS(i, 30, 21) << 0); \
  *imm = SEXT(*imm, 20) << 1; \
} while (0)
#define immSB() do{ \
  *imm = SEXT( \
      (BITS(i, 31, 31) << 11) |  \
      (BITS(i, 7, 7)   << 10) |  \
      (BITS(i, 30, 25) << 4)  |  \
      (BITS(i, 11, 8)  << 0),    \
      12 \
  ) << 1; \
} while (0)
static void decode_operand(Decode *s, int *rd, word_t *src1, word_t *src2, word_t *imm, int type) {
  uint32_t i = s->isa.inst;
  int rs1 = BITS(i, 19, 15);
  int rs2 = BITS(i, 24, 20);
  *rd     = BITS(i, 11, 7);
  switch (type) {
    case TYPE_I: 
    {
      src1R();          
      immI();

      break;
    }
    case TYPE_U:                   immU(); break;
    case TYPE_S: src1R(); src2R(); immS(); break;
    case TYPE_R: {
      src1R();
      src2R();
      debug("test %d", 1);
      break;
    }
    case TYPE_SB: {
      break;
    }
    case TYPE_UJ: {
      immJ();

      break;
    }
    case TYPE_N: break;
    default: panic("unsupported type = %d", type);
  }
}

static int decode_exec(Decode *s) {
  s->dnpc = s->snpc;

#define INSTPAT_INST(s) ((s)->isa.inst)
#define INSTPAT_MATCH(s, name, type, ... /* execute body */ ) { \
  int rd = 0; \
  word_t src1 = 0, src2 = 0, imm = 0; \
  decode_operand(s, &rd, &src1, &src2, &imm, concat(TYPE_, type)); \
  __VA_ARGS__ ; \
}
  //没有理解当前地址pc和下一条地址pc之间的关系,导致错误,应该用当前pc预测下一条dpc, 之后用动态的pc更新pc
  INSTPAT_START();
  INSTPAT("??????? ????? ????? 000 ????? 0010011", addi    , I, R(rd) = src1 + imm);
  INSTPAT("0000000 ????? ????? 001 ????? 0010011", slli    , I, R(rd) = src1 << imm);
  INSTPAT("??????? ????? ????? 010 ????? 0010011", slti    , I, R(rd) = (src1 < imm) ? 1 : 0);
  INSTPAT("??????? ????? ????? 011 ????? 0010011", sltiu    , I, R(rd) = ((uint32_t)src1 < imm) ? 1 : 0);

  INSTPAT("??????? ????? ????? 100 ????? 0010011", xori    , I, R(rd) = src1 ^ imm);
  INSTPAT("0000000 ????? ????? 101 ????? 0010011", srli    , I, R(rd) = (uint32_t)src1 >> imm);
  INSTPAT("0100000 ????? ????? 101 ????? 0010011", srai    , I, R(rd) = src1 >> imm);

  INSTPAT("??????? ????? ????? 110 ????? 0010011", ori    , I, R(rd) = src1 | imm);
  INSTPAT("??????? ????? ????? 111 ????? 0010011", andi    , I, R(rd) = src1 & imm);

  INSTPAT("??????? ????? ????? 100 ????? 00000 11", lbu    , I, R(rd) = Mr(src1 + imm, 1));
  INSTPAT("??????? ????? ????? 000 ????? 00000 11", lb    , I, R(rd) = Mr(src1 + imm, 1));
  INSTPAT("??????? ????? ????? 001 ????? 00000 11", lh    , I, R(rd) = Mr(src1 + imm, 2));
  INSTPAT("??????? ????? ????? 010 ????? 00000 11", lw    , I, R(rd) = Mr(src1 + imm, 4));
  

  INSTPAT("??????? ????? ????? 000 ????? 1100111", jalr    , I, R(rd) = s->pc + 4, s->dnpc = src1 + imm);

  INSTPAT("0000000 ????? ????? 000 ????? 0110011", add    , R, R(rd) = src1 + src2);
  INSTPAT("??????? ????? ????? ??? ????? 1101111", jal  , UJ, R(rd) = s->pc + 4, s->dnpc = s->pc + imm);


  INSTPAT("??????? ????? ????? 000 ????? 01000 11", sb     , S, Mw(src1 + imm, 1, src2));
  INSTPAT("??????? ????? ????? 001 ????? 01000 11", sh     , S, Mw(src1 + imm, 2, src2));
  INSTPAT("??????? ????? ????? 010 ????? 01000 11", sw     , S, Mw(src1 + imm, 4, src2));
  
  INSTPAT("??????? ????? ????? ??? ????? 00101 11", auipc  , U, R(rd) = s->pc + imm);
  INSTPAT("??????? ????? ????? ??? ????? 01101 11", lui  , U, R(rd) = imm);

  INSTPAT("??????? ????? ????? 000 ????? 1100011", beq  , SB, (src1 == src2 ? s->dnpc = s->pc + imm : s->dnpc));
  INSTPAT("??????? ????? ????? 001 ????? 1100011", bne  , SB, (src1 != src2 ? s->dnpc = s->pc + imm : s->dnpc));
  INSTPAT("??????? ????? ????? 100 ????? 1100011", blt  , SB, (src1 < src2 ? s->dnpc = s->pc + imm : s->dnpc));
  INSTPAT("??????? ????? ????? 101 ????? 1100011", bge  , SB, (src1 > src2 ? s->dnpc = s->pc + imm : s->dnpc));

  INSTPAT("0000000 00001 00000 000 00000 11100 11", ebreak , N, NEMUTRAP(s->pc, R(10))); // R(10) is $a0
  INSTPAT("??????? ????? ????? ??? ????? ????? ??", inv    , N, INV(s->pc));
  INSTPAT_END();

  R(0) = 0; // reset $zero to 0

  return 0;
}

int isa_exec_once(Decode *s) {
  s->isa.inst = inst_fetch(&s->snpc, 4);
  return decode_exec(s);
}
